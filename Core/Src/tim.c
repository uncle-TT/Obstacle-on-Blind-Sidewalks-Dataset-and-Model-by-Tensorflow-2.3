void start(void)
{
    __HAL_TIM_SetCounter(&htim6,0);   //清0计数
    HAL_TIM_Base_Start_IT(&htim6);   // 开启定时器更新中断       
}

float get_num(void)
{  
   unsigned int  num,dta;
   HAL_TIM_Base_Stop_IT (&htim6);       //关闭定时器更新中断
   num = __HAL_TIM_GetCounter(&htim6);  //获得计数值
   dta = (num*0.1*34)/2;                     //cm为单位
    return dta;
}
